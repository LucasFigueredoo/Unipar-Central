package br.unipar.central.models;

/**
 *
 * @author 00240578
 */
public class Banco extends AbstractBaseEntity{
    
    private String nome;

    public Banco() {
    }

    public Banco(String nome) {
        this.nome = nome;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    @Override
    public String toString() {
        return "Banco{" + "nome=" + nome + '}';
    }
    
}
